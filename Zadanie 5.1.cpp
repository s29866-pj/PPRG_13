#include <iostream>
#include <vector>

int main() {
    // a) liczba elementow 
    std::cout << "Podaj liczbe elementow wektora: ";
    int n;
    std::cin >> n;

    // b) pobranie elementow 
    std::vector<int> vector(n);
    for (int i = 0; i < n; ++i) {
        std::cout << "Podaj element wektora na pozycji " << i << ": ";
        std::cin >> vector[i];
    }

    // 3 wartość elementu maksymalnego 
    int maxElement = vector[0];
    for (int i = 1; i < n; ++i) {
        if (vector[i] > maxElement) {
            maxElement = vector[i];
        }
    }
    std::cout << "Wartosc elementu maksymalnego: " << maxElement << std::endl;

    // 4 Numer indeksu
    int maxIndex = 0;
    for (int i = 1; i < n; ++i) {
        if (vector[i] > vector[maxIndex]) {
            maxIndex = i;
        }
    }
    std::cout << "Element maksymalny: " << maxIndex << std::endl;

    return 0;
}
